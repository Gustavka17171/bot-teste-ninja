import 'dotenv/config';
import {
  Client,
  GatewayIntentBits,
  EmbedBuilder,
  ButtonBuilder,
  ButtonStyle,
  ActionRowBuilder,
  Events,
  REST,
  Routes
} from 'discord.js';

// ===== CONFIG =====
const TOKEN = process.env.TOKEN;
const CLIENT_ID = process.env.CLIENT_ID;
// ==================

const client = new Client({
  intents: [GatewayIntentBits.Guilds]
});

// Slash command
const commands = [
  {
    name: 'setup',
    description: 'Enviar painel de compra'
  }
];

// Registrar slash
const rest = new REST({ version: '10' }).setToken(TOKEN);
(async () => {
  try {
    await rest.put(
      Routes.applicationCommands(CLIENT_ID),
      { body: commands }
    );
    console.log('âœ… Comandos registrados');
  } catch (err) {
    console.error(err);
  }
})();

client.once(Events.ClientReady, () => {
  console.log(`ðŸ¤– Bot online como ${client.user.tag}`);
});

// InteraÃ§Ãµes
client.on(Events.InteractionCreate, async interaction => {

  // Slash command
  if (interaction.isChatInputCommand()) {
    if (interaction.commandName === 'setup') {

      const embed = new EmbedBuilder()
        .setTitle('ðŸ›’ Loja Oficial')
        .setDescription('Clique no botÃ£o abaixo para comprar')
        .setColor(0x00ff99)
        .setFooter({ text: 'Sistema automÃ¡tico' });

      const button = new ButtonBuilder()
        .setCustomId('comprar')
        .setLabel('Comprar')
        .setStyle(ButtonStyle.Success);

      const row = new ActionRowBuilder().addComponents(button);

      await interaction.reply({
        embeds: [embed],
        components: [row]
      });
    }
  }

  // BotÃ£o
  if (interaction.isButton()) {
    if (interaction.customId === 'comprar') {
      await interaction.reply({
        content: 'âœ… Compra iniciada! (ticket/pagamento entra aqui)',
        ephemeral: true
      });
    }
  }
});

client.login(TOKEN);
