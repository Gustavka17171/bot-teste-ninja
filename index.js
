const {
  Client,
  GatewayIntentBits,
  EmbedBuilder,
  ActionRowBuilder,
  ButtonBuilder,
  ButtonStyle,
  REST,
  Routes,
  Events
} = require("discord.js");

const config = require("./config");

// =========================
// CLIENTE
// =========================
const client = new Client({
  intents: [GatewayIntentBits.Guilds]
});

// =========================
// SLASH COMMANDS
// =========================
const commands = [
  {
    name: "setup",
    description: "Enviar painel de compra"
  }
];

// Registrar comandos automaticamente
const rest = new REST({ version: "10" }).setToken(config.token);

(async () => {
  try {
    console.log("â³ Registrando comandos...");
    await rest.put(
      Routes.applicationCommands(config.clientId),
      { body: commands }
    );
    console.log("âœ… Comandos registrados");
  } catch (error) {
    console.error("âŒ Erro ao registrar comandos:", error);
  }
})();

// =========================
// BOT ONLINE
// =========================
client.once(Events.ClientReady, () => {
  console.log(`ðŸ¤– Bot online como ${client.user.tag}`);
});

// =========================
// INTERAÃ‡Ã•ES
// =========================
client.on(Events.InteractionCreate, async interaction => {

  // Slash command
  if (interaction.isChatInputCommand()) {
    if (interaction.commandName === "setup") {

      const embed = new EmbedBuilder()
        .setTitle("ðŸ›’ Loja Oficial")
        .setDescription("Clique no botÃ£o abaixo para iniciar a compra.")
        .setColor(0x00ff99)
        .setFooter({ text: "Sistema automÃ¡tico" });

      const button = new ButtonBuilder()
        .setCustomId("comprar")
        .setLabel("Comprar")
        .setStyle(ButtonStyle.Success);

      const row = new ActionRowBuilder().addComponents(button);

      await interaction.reply({
        embeds: [embed],
        components: [row]
      });
    }
  }

  // BotÃ£o
  if (interaction.isButton()) {
    if (interaction.customId === "comprar") {
      await interaction.reply({
        content: "âœ… Compra iniciada! (ticket e pagamento entram aqui)",
        ephemeral: true
      });
    }
  }
});

// =========================
// LOGIN
// =========================
client.login(config.token);
